# ⏤ { pre-settings } ⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤

class accept_rules(commands.Cog):
    def __init__(self, bot):
        self.bot: commands.Bot = bot

# ⏤ { codebase } ⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤

    @commands.Cog.slash_command(
        name="accept_rules",
        description="Accept the rules on the Discord server."
    )
    async def server_information(self, ctx):
        current_datetime = datetime.now().strftime("%d.%m.%y um %H:%M Uhr")
        channel = self.bot.get_channel(config.accept_rules_channel)

        accept_rules_embed = discord.Embed(
            color=0xff6a00,
            title=f"{ctx.author.name} has accepted the rules.",
            description=f"Username: {ctx.author.mention}\n"
                        f"UserID: `{ctx.author.id}`\n"
                        f"Date: `{current_datetime}`"
        )
        await ctx.respond(f"You have accepted the rules on the Discord.", hidden=True)
        await channel.send(embed=accept_rules_embed)

# ⏤ { pre-settings } ⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤⏤

def setup(coffee_bot):
    coffee_bot.add_cog(accept_rules(coffee_bot))
